private void getPrecio(){
    if(jchbTerminosCondiciones.isSelected()){
        int base = Integer.parseInt(jlbPrecio.getText());
        int estampado = Integer.parseInt(jlbPrecioEst.getText());
        int cantidad = Integer.parseInt(jtxtCantidadTotal.getText());
        int subtotal =0;
        subtotal += base;
        if(jtbnEstampado.isSelected()){
            subtotal += estampado;
        }
        int total = subtotal * cantidad;
        jtxtCosto.setText(String.valueOf(total));
        
    }else{
        jchbTerminosCondiciones.setBackground(Color.RED);
        jchbTerminosCondiciones.setOpaque(true);
    }
}
